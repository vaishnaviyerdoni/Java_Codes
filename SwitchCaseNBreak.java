public class SwitchCaseNBreak {
    public static void main(String[] args){
        int day = 5;
        switch (day){
            case 6:
                System.out.println("Yay! It is Saturday!!!");
                break;
            
            case 7:
                System.out.println("Yay! It is Sunday!!!");
                break;

            default:
                System.out.println("Wishing it was a Weekend :(");
            
        }

    }
    
}
